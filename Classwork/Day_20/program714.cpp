#include<iostream>
using namespace std;

bool CheckPerfect(int iNo)
{

    static int iCnt = 1;
    static int iSum = 0;
    
    if(iCnt<=iNo/2)
    {
        if(iNo % iCnt == 0)
        {
            iSum = iSum + iCnt;
        }
        iCnt++;

        CheckPerfect(iNo);
    }
    if(iSum == iNo)
    {
        return true;
    }
    else
    {
        return false;
    }
}

int main()

{
    int iValue = 0;
    bool bRet = false;

    cout<<"Enter the number : \n";
    cin>>iValue;

    bRet = CheckPerfect(iValue);

    if(bRet == true)
    {
        cout<<"Its a Perfect Number";
    }
    else
    {
        cout<<"Its not a Perfect Number";
    }

    return 0;
}