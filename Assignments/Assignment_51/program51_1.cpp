#include <iostream>
using namespace std;

#pragma pack(1)

/////////////////////////////////////////////////////////////////////////////////
//
// Function Name :  Display
// Input:           Value of any data type, number of times
// Output:          Displays the values
// Description:     Use to display given value specified number of times
// Author:          Sakshi Ravindra Darandale
// Date:            11/01/2026
//
////////////////////////////////////////////////////////////////////////////////

template <class T>
void Display(T iValue,int iSize)
{
    int iCnt=0;
    
    for(iCnt=0;iCnt<iSize;iCnt++)
    {
        cout<<iValue<<"\t";
    } 
    cout<<"\n";
}
////////////////////////////////////////////////////////////////////////////////
//      Entry of main function
///////////////////////////////////////////////////////////////////////////////
int main()
{
    Display('M',7);
    Display(11,3);
    Display(3.7,6);
    
    return 0;
}
