#include<iostream>
using namespace std;

/////////////////////////////////////////////////////////////////////////////////
//
// Function Name : Swap
// Input         : Two values of same data type 
// Output        : Swaps the values
// Description   : Exchanges two generic values using a temporary variable
// Author        : Sakshi Ravindra Darandale
// Date          : 12/01/2026
//
///////////////////////////////////////////////////////////////////////////////

template<class T>
void Swap(T &no1,T &no2)
{

   T temp;
   
   temp=no1;
   no1=no2;
   no2=temp;
  
}

/////////////////////////////////////////////////////////////////////////////////
//
// Function Name : MainX
// Input         : None
// Output        : Displays swapped two values
// Description   : Accepts two values from user and calls Swap function
// Author        : Sakshi Ravindra Darandale
// Date          : 12/01/2026
//
/////////////////////////////////////////////////////////////////////////////////


template<class T>
void MainX()
{
    T Value1;
    T Value2;
    
    cout<<"Enter the two numbers :\n";
    cin>>Value1>>Value2;
    
    Swap(Value1,Value2);
    
    cout<<"After Swapping \n";
    cout<<Value1<<" "<<Value2<<"\n";
  
}

/////////////////////////////////////////////////////////////////////////////////
//      Entry of main function
/////////////////////////////////////////////////////////////////////////////////

int main()
{
    MainX<int>(); 

    return 0;
}
